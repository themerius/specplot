class IconFetcher:
    """Fetches Material Symbols icons from Google's CDN."""

    def __init__(self, cache_dir: Path | None = None):
        self.cache_dir = cache_dir or CACHE_DIR
        self.cache_dir.mkdir(parents=True, exist_ok=True)
        self._client: httpx.Client | None = None

    @property
    def client(self) -> httpx.Client:
        if self._client is None:
            self._client = httpx.Client(timeout=10.0)
        return self._client

    def _cache_path(self, icon_name: str) -> Path:
        """Get the cache path for an icon."""
        safe_name = hashlib.md5(icon_name.encode()).hexdigest()
        return self.cache_dir / f"{safe_name}.svg"

    def _extract_path(self, svg_content: str) -> str | None:
        """Extract the path data from an SVG."""
        # Try to find path element
        match = re.search(r'd="([^"]+)"', svg_content)
        if match:
            return match.group(1)
        return None

    def get_icon_path(self, icon_name: str) -> str:
        """Get the SVG path data for an icon.

        Args:
            icon_name: Material Symbols icon name (e.g., 'database', 'person')

        Returns:
            SVG path data string
        """
        # Check cache first
        cache_path = self._cache_path(icon_name)
        if cache_path.exists():
            svg_content = cache_path.read_text()
            path_data = self._extract_path(svg_content)
            if path_data:
                return path_data

        # Try to fetch from CDN
        url = ICON_URL_TEMPLATE.format(icon=icon_name)
        try:
            response = self.client.get(url)
            if response.status_code == 200:
                svg_content = response.text
                # Cache it
                cache_path.write_text(svg_content)
                path_data = self._extract_path(svg_content)
                if path_data:
                    return path_data
        except Exception:
            pass

        # Fall back to built-in icons
        if icon_name in FALLBACK_ICONS:
            return FALLBACK_ICONS[icon_name]

        # Ultimate fallback: a simple circle
        return "M12 2C6.48 2 2 6.48 2 12s4.48 10 10 10 10-4.48 10-10S17.52 2 12 2z"

    def close(self):
        """Close the HTTP client."""
        if self._client:
            self._client.close()
            self._client = None


# Global icon fetcher instance
_icon_fetcher: IconFetcher | None = None


def get_icon_path(icon_name: str) -> str:
    """Get the SVG path for a Material Symbols icon."""
    global _icon_fetcher
    if _icon_fetcher is None:
        _icon_fetcher = IconFetcher()
    return _icon_fetcher.get_icon_path(icon_name)
